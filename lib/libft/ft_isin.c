#include "libft.h"

size_t	ft_isin(char *arr, char c, size_t n)
{
	size_t	i;

	if (!arr)
		return (0);
	i = 0;
	while (i < n)
	{
		if (arr[i] == c)
			return (1);
		i++;
	}
	return (0);
}
